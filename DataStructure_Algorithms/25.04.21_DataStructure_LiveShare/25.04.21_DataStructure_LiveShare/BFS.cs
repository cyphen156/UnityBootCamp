using System;
using System.Collections.Generic;
using System.Linq;
using System.Linq.Expressions;
using System.Text;
using System.Threading.Tasks;

namespace _25._04._21_DataStructure_LiveShare
{
    internal class BFS
    {
        public static int result = 0;
        public static int Visit(List<int[]> arr, int row, int col)
        {
            // 자료가 입력되지 않은 공백
            if (arr[row][col] == 0)
            {
                return result;
            }
            return result;
        }
    }
}
